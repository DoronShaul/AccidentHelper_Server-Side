/**
 * this class represents an accident event.
 */
public class AccidentEvent {
	private int police, mda, fire, contacts, severity, tow, injured, blockedRoad;
	private String email;
	public int getPolice() {
		return police;
	}
	public void setPolice(int police) {
		this.police = police;
	}
	public int getMda() {
		return mda;
	}
	public void setMda(int mda) {
		this.mda = mda;
	}
	public int getFire() {
		return fire;
	}
	public void setFire(int fire) {
		this.fire = fire;
	}
	public int getContacts() {
		return contacts;
	}
	public void setContacts(int contacts) {
		this.contacts = contacts;
	}
	public int getSeverity() {
		return severity;
	}
	public void setSeverity(int severity) {
		this.severity = severity;
	}
	public int getInjured() {
		return injured;
	}
	public void setInjured(int injured) {
		this.injured = injured;
	}
	public int getBlockedRoad() {
		return blockedRoad;
	}
	public void setBlockedRoad(int blockedRoad) {
		this.blockedRoad = blockedRoad;
	}
	public int getTow() {
		return tow;
	}
	public void setTow(int tow) {
		this.tow = tow;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	

}
